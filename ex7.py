#Desenvolva um código que leia a altura e o peso de uma pessoa e calcule o imc.
peso = float(input("Informe qual é o seu peso atual: "))
altura = float(input("Informe qual é a sua altura atual: "))
imc = peso / (altura * altura)
print(f"O seu índice de massa corporal é {round(imc,2)}")
print(f"O seu índice de massa corporal é {imc:.2f}")
